async function main() {
  console.log('üå± Seeding translations...')

  // Image URLs (same for all languages)
  const imageUrls = {
    'product.image1': 'https://images.unsplash.com/photo-1556228578-8c89e6adf883?w=1200&q=80',
    'product.image2': 'https://images.unsplash.com/photo-1516733968668-dbdce39c4651?w=1200&q=80',
  }

  let totalSeeded = 0

  // Seed content for each language
  for (const [lang, langTranslations] of Object.entries(translations)) {
    for (const [key, value] of Object.entries(langTranslations)) {
      const metadata = contentMetadata[key]
      
      // Skip if metadata is missing
      if (!metadata) {
        console.warn(`‚ö†Ô∏è  Missing metadata for key: ${key}, skipping...`)
        continue
      }
      
      const finalValue = key in imageUrls ? imageUrls[key as keyof typeof imageUrls] : value

      await prisma.content.upsert({
        where: {
          key_language: {
            key,
            language: lang,
          },
        },
        update: {
          value: finalValue,
          type: metadata.type,
          page: metadata.page,
          section: metadata.section,
          label: metadata.label,
          description: metadata.description,
        },
        create: {
          key,
          language: lang,
          type: metadata.type,
          value: finalValue,
          page: metadata.page,
          section: metadata.section,
          label: metadata.label,
          description: metadata.description,
        },
      })
      totalSeeded++
    }
  }

  // Also seed image content for all languages (images are the same)
  for (const lang of ['en', 'ms', 'zh-CN']) {
    for (const [key, url] of Object.entries(imageUrls)) {
      const metadata = contentMetadata[key]
      await prisma.content.upsert({
        where: {
          key_language: {
            key,
            language: lang,
          },
        },
        update: {},
        create: {
          key,
          language: lang,
          type: metadata.type,
          value: url,
          page: metadata.page,
          section: metadata.section,
          label: metadata.label,
          description: metadata.description,
        },
      })
      totalSeeded++
    }
  }

  const defaultContactLinks = [
    { label: 'Email', url: 'https://sweetb.co', description: 'Reach us over email', logoUrl: 'https://cdn.simpleicons.org/gmail/EA4335', accentColor: '#EA4335', sortOrder: 1 },
    { label: 'Phone Number', url: 'https://sweetb.co', description: 'Speak with SweetB support', logoUrl: null, accentColor: '#F8F8F8', sortOrder: 2 },
    { label: 'WhatsApp', url: 'https://sweetb.co', description: 'Chat via WhatsApp', logoUrl: 'https://cdn.simpleicons.org/whatsapp/25D366', accentColor: '#25D366', sortOrder: 3 },
    { label: 'Telegram', url: 'https://sweetb.co', description: 'Message us on Telegram', logoUrl: 'https://cdn.simpleicons.org/telegram/26A5E4', accentColor: '#26A5E4', sortOrder: 4 },
    { label: 'Facebook', url: 'https://sweetb.co', description: 'Follow SweetB on Facebook', logoUrl: 'https://cdn.simpleicons.org/facebook/1877F2', accentColor: '#1877F2', sortOrder: 5 },
    { label: 'Instagram', url: 'https://sweetb.co', description: 'DM us on Instagram', logoUrl: 'https://cdn.simpleicons.org/instagram/E4405F', accentColor: '#E4405F', sortOrder: 6 },
    { label: 'TikTok', url: 'https://sweetb.co', description: 'Connect on TikTok', logoUrl: 'https://cdn.simpleicons.org/tiktok/000000', accentColor: '#000000', sortOrder: 7 },
  ]

  for (const contact of defaultContactLinks) {
    const existing = await prisma.contactLink.findFirst({
      where: { label: contact.label },
    })

    if (existing) {
      await prisma.contactLink.update({
        where: { id: existing.id },
        data: contact,
      })
    } else {
      await prisma.contactLink.create({
        data: contact,
      })
    }
  }

  console.log(`‚úÖ Translations seeded: ${totalSeeded} items across 3 languages`)
  console.log(`‚úÖ Contact links seeded: ${defaultContactLinks.length}`)
}

main()
